let keyInputs = {forward:"w", back:"s", left:"a", right:"d", menu:"c", yes:"enter", no:"escape"};

for (let i = 0; i < 15; i++) {
    let row = document.createElement('div');
    row.classList.add('x');
    for (let j = 0; j < 15; j++) {
        let tile = document.createElement('div');
        tile.classList.add('y');
        row.appendChild(tile);
    }
    gameWindow.appendChild(row);
}

document.addEventListener('keydown', function (e) {
    switch (e.key.toLowerCase()) {
        case keyInputs.forward:
            playerPos[1] -= 1;
            smoothWalk(0);
            drawMap();
            break;

        case keyInputs.back:
            playerPos[1] += 1;
            smoothWalk(1);
            drawMap();
            break;

        case keyInputs.left:
            playerPos[0] -= 1;
            smoothWalk(2);
            drawMap();
            break;

        case keyInputs.right:
            playerPos[0] += 1;
            smoothWalk(3);
            drawMap();
            break;
    }
});

drawMap();

function drawMap() {
    for (let i = 0; i < 15; i++) {
        for (let j = 0; j < 15; j++) {
            let x = i + playerPos[0] - 7;
            let y = j + playerPos[1] - 7;
            switch (world[x][y]) {
                case 0:
                    gameWindow.getElementsByClassName('x')[i].getElementsByClassName('y')[j].classList.remove('grass', 'stone');
                    break;
                case 1:
                    gameWindow.getElementsByClassName('x')[i].getElementsByClassName('y')[j].classList.add('grass');
                    gameWindow.getElementsByClassName('x')[i].getElementsByClassName('y')[j].classList.remove('stone');
                    break;
                case 2:
                    gameWindow.getElementsByClassName('x')[i].getElementsByClassName('y')[j].classList.add('stone');
                    gameWindow.getElementsByClassName('x')[i].getElementsByClassName('y')[j].classList.remove('grass');
                    break;
            }
        }
    }
}

function smoothWalk(direction) {
    if (direction === 0) {
        gameWindow.style.translate = "0 calc(100vh / 15)";
        gameWindow.style.transition = "0.5s";
    } else if (direction === 1) {
        gameWindow.style.translate = "0 -calc(100vh / 15)";
        gameWindow.style.transition = "0.5s";
    } else if (direction === 2) {
        gameWindow.style.translate = "calc(100vh / 15) 0";
        gameWindow.style.transition = "0.5s";
    } else if (direction === 3) {
        gameWindow.style.translate = "-calc(100vh / 15) 0";
        gameWindow.style.transition = "0.5s";
    }
}